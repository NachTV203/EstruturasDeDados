# Exemplo de lista, uma estrutura linear
# Vamos criar uma lista com alguns números
numeros = [10, 20, 30, 40, 50]

# Acessando os elementos da lista pelo índice
print(numeros[0])  # Acessa o primeiro item (10)
print(numeros[3])  # Acessa o quarto item (40)

# Adicionando um novo número no final da lista
numeros.append(60)
print(numeros)  # Agora a lista vai ser [10, 20, 30, 40, 50, 60]
